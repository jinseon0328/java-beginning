/* 반복문 02 - while
for와는 다르게 while문은 조건이 만족되는 동안
계속 반복이 된다.
 */
public class Ex03While{
    public static void main(String[]args){


 /* for(int i =1; i<=3; i++){
     System.out.println(i)
 }
 를 while로 만들면 다음과 같은 모양이 된다.
  */
  int i = 1;
  while(i<=3){
      System.out.println(i);
      i++;
  }
 }
}